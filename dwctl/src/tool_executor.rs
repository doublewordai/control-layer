//! HTTP-based tool executor for server-side tool calling.
//!
//! [`HttpToolExecutor`] implements `onwards::ToolExecutor` by POSTing tool arguments as JSON to
//! a configured HTTP endpoint.  It is constructed per-request with the resolved tool definitions
//! for that particular request (deployment × group intersection).
//!
//! # Per-request tool resolution
//!
//! Because onwards holds a *single* `Arc<dyn ToolExecutor>` in its shared `AppState`, making it
//! per-request requires an indirection.  We use a `DashMap` inside `PerRequestToolRegistry` keyed
//! by a per-request UUID that is:
//!
//! 1. Generated by the [`tool_injection`] middleware.
//! 2. Written into the registry alongside the resolved `ToolDefinition` map.
//! 3. Forwarded to onwards via a private request header (`x-dw-tool-request-id`).
//! 4. Read back inside `ToolExecutor::execute` from `tokio::task_local!` storage (set by the
//!    middleware scope).
//!
//! The registry cleans up its entry once the request completes (RAII via `scopeguard`).
//!
//! # Analytics
//!
//! Each tool call records a `tool_call_analytics` row via a fire-and-forget background write.

use async_trait::async_trait;
use chrono::Utc;
use dashmap::DashMap;
use onwards::traits::{ToolError, ToolExecutor};
use serde_json::Value;
use sqlx::PgPool;
use std::collections::HashMap;
use std::sync::Arc;
use std::time::Instant;
use tracing::{info_span, instrument, Instrument};
use uuid::Uuid;

// ---------------------------------------------------------------------------
// Per-tool configuration
// ---------------------------------------------------------------------------

/// Per-tool configuration resolved from the database.
#[derive(Debug, Clone)]
pub struct ToolDefinition {
    /// URL to POST tool arguments to.
    pub url: String,
    /// Optional Bearer token for the `Authorization` header.
    pub api_key: Option<String>,
    /// HTTP request timeout in seconds.
    pub timeout_secs: u64,
    /// Foreign key into `tool_sources` for analytics.
    pub tool_source_id: Uuid,
}

/// Full set of tools resolved for a single request.
#[derive(Debug, Clone)]
pub struct ResolvedToolSet {
    /// Resolved tool definitions: name → config.
    pub tools: HashMap<String, ToolDefinition>,
    /// Tool source metadata for schema injection: name → (description, parameters).
    pub metadata: HashMap<String, (Option<String>, Option<Value>)>,
}

impl ResolvedToolSet {
    pub fn new(
        tools: HashMap<String, ToolDefinition>,
        metadata: HashMap<String, (Option<String>, Option<Value>)>,
    ) -> Self {
        Self { tools, metadata }
    }

    pub fn is_empty(&self) -> bool {
        self.tools.is_empty()
    }

    /// Build the OpenAI tool schema array from this resolved tool set.
    ///
    /// Each entry looks like:
    /// ```json
    /// {"type": "function", "function": {"name": "...", "description": "...", "parameters": {...}}}
    /// ```
    pub fn to_openai_schemas(&self) -> Vec<Value> {
        self.tools
            .keys()
            .map(|name| {
                let (description, parameters) = self.metadata.get(name).cloned().unwrap_or((None, None));

                let mut function = serde_json::json!({"name": name});

                if let Some(desc) = description {
                    function["description"] = Value::String(desc);
                }

                if let Some(params) = parameters {
                    function["parameters"] = params;
                }

                serde_json::json!({"type": "function", "function": function})
            })
            .collect()
    }
}

// ---------------------------------------------------------------------------
// Per-request registry
// ---------------------------------------------------------------------------

/// Thread-safe registry mapping request-IDs to their resolved tool sets.
///
/// Shared as `Arc<PerRequestToolRegistry>` in `AppState`.
#[derive(Debug, Default)]
pub struct PerRequestToolRegistry {
    entries: DashMap<Uuid, Arc<ResolvedToolSet>>,
}

impl PerRequestToolRegistry {
    pub fn new() -> Self {
        Self::default()
    }

    /// Insert a resolved tool set for `request_id`.
    pub fn insert(&self, request_id: Uuid, tool_set: ResolvedToolSet) {
        self.entries.insert(request_id, Arc::new(tool_set));
    }

    /// Remove the entry for `request_id` (called on request completion).
    pub fn remove(&self, request_id: Uuid) {
        self.entries.remove(&request_id);
    }

    /// Look up the tool set for `request_id`.
    pub fn get(&self, request_id: Uuid) -> Option<Arc<ResolvedToolSet>> {
        self.entries.get(&request_id).map(|e| e.clone())
    }
}

