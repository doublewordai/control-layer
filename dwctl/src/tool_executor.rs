    #[tokio::test]
    async fn test_can_handle_returns_true_for_registered_tools() {
        let registry = Arc::new(PerRequestToolRegistry::new());
        let request_id = Uuid::new_v4();
        let mut tools = HashMap::new();
        tools.insert(
            "my_tool".to_string(),
            ToolDefinition {
                url: "http://example.com".to_string(),
                api_key: None,
                timeout_secs: 5,
                tool_source_id: Uuid::nil(),
            },
        );
        registry.insert(request_id, ResolvedToolSet::new(tools, HashMap::new()));

        let client = reqwest::Client::new();
        let executor = HttpToolExecutor::new(registry, client, None);

        let result = CURRENT_TOOL_REQUEST_ID.scope(Some(request_id), async {
            (executor.can_handle("my_tool"), executor.can_handle("unknown"))
        })
        .await;

        assert!(result.0);
        assert!(!result.1);
    }

